// Condição com Switch-Case: facilita a escrita de trechos de programa em que a seleção deve ser 
//feita entre várias alternativas //

//Escreva um algoritmo que imprima uma palavra que começa com uma determinada letra//

var letra = 'A';

switch (letra) {
    case 'A':
        console.log('Arara');
        break;
    case 'B':
        console.log('Biscoito');
        break;
    case 'C':
        console.log('Camila');
        break;
    default:
        console.log('Letra não cadastrada!')
        }
    

